#include "mock_credential_storage.hpp"
#include "openssl_security_module.hpp"
#include <gtest/gtest.h>

using vanetza::ByteBuffer;
using namespace vanetza::pki;

/**
 * Test vectors from IEEE 1609.2-2017 (Annex D)
 */

class SecurityModuleTest : public ::testing::Test
{
public:
    void SetUp() override
    {
        credentials = std::make_shared<MockCredentialStorage>();
        security = std::make_shared<OpenSslSecurityModule>(credentials);
    }

protected:
    std::shared_ptr<CredentialStorage> credentials;
    std::shared_ptr<SecurityModule> security;
};

TEST_F(SecurityModuleTest, mac_vector1)
{
    const ByteBuffer key(20, 0x0B);
    const ByteBuffer msg { 0x48, 0x69, 0x20, 0x54, 0x68, 0x65, 0x72, 0x65 };
    const ByteBuffer tag {
        0xB0, 0x34, 0x4C, 0x61, 0xD8, 0xDB, 0x38, 0x53,
        0x5C, 0xA8, 0xAF, 0xCE, 0xAF, 0x0B, 0xF1, 0x2B
    };
    EXPECT_EQ(tag, security->calculate_hmac_sha256(key, msg));
}

